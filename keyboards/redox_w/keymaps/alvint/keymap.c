layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _QWERTY:
            set_led_off;
            break;
        case _PROGRAMMING:
            set_led_green;
            break;
        case _GAMING:
            set_led_blue;
            break;
        default:
            break;
    }
  return state;
}


